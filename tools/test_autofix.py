"""
Самопроверка детерминированных правок autofix.py (без pytest).

Покрывает правки от 3 июня 2026 (фикс всплеска «ошибок правописания» text.ru):
- расцепление дефисных сцепок субъектов;
- защита от ложных срабатываний (термины, география);
- де-ёфикация ё→е;
- сокращения (маткапитал и пр.).

Запуск:
    python -m tools.test_autofix
Exit: 0 — все кейсы прошли, 1 — есть провалы.
"""

from __future__ import annotations

import sys

from tools.autofix import fix_text

if hasattr(sys.stdout, "reconfigure"):
    try:
        sys.stdout.reconfigure(encoding="utf-8")
    except Exception:
        pass


# (вход, обязано_быть_в_выходе, НЕ_должно_встречаться_в_выходе или None)
CASES = [
    # --- расцепление: роль + тип лица → роль ---
    ("Кредитор-юридическое лицо подал заявление", "Кредитор подал заявление", "юридическое"),
    ("требование кредитора-юридического лица", "требование кредитора", "-юридическ"),
    ("кредиторов-юридических лиц включили в реестр", "кредиторов включили в реестр", "-юридическ"),
    ("кредитора-юрлица в деле", "кредитора в деле", "юрлиц"),
    ("должник-физическое лицо", "должник", "физическое"),
    # --- расцепление: роль + роль → вторая (уточняющая) часть ---
    ("требование кредитора-поставщика", "требование поставщика", "кредитора-"),
    ("организация-должник обязана", "должник обязана", "организация-"),
    ("у компании-должника нет денег", "у должника нет денег", "компании-"),
    ("контрагент-взыскатель подал иск", "взыскатель подал иск", "контрагент-"),
    ("должника-гражданина признали банкротом", "гражданина признали банкротом", "должника-"),
    # --- капитализация в начале предложения сохраняется ---
    ("Покупатель-банкрот не платит", "Банкрот не платит", "Покупатель-"),
    ("Фирма-кредитор требует возврата", "Кредитор требует возврата", "Фирма-"),
    # --- НЕ трогать: термины и география (вторая часть НЕ роль) ---
    ("счета-фактуры оформлены вовремя", "счета-фактуры", None),
    ("Западно-Сибирского округа суд", "Западно-Сибирского", None),
    ("дизайн-макеты переданы франчайзи", "дизайн-макеты", None),
    ("каких-либо возражений нет", "каких-либо", None),
    ("мастер-партнёра франшизы уведомили", "мастер-партнера", None),  # ё→е, дефис не трогаем
    # --- де-ёфикация ---
    ("расчёт неустойки, платёж и заём", "расчет неустойки, платеж и заем", "ё"),
    # --- сокращения ---
    ("выплатили маткапитал и маткапитала", "материнский капитал", "маткапитал"),
    ("долг юрлица перед физлицом", "юридического лица", "юрлиц"),
]


def run() -> int:
    failures = []
    for inp, must, mustnot in CASES:
        out, _ = fix_text(inp, is_html=False)
        ok = (must in out) and (mustnot is None or mustnot not in out)
        if not ok:
            failures.append((inp, out, must, mustnot))

    total = len(CASES)
    passed = total - len(failures)
    print(f"autofix selftest: {passed}/{total} прошло")
    for inp, out, must, mustnot in failures:
        print(f"  [FAIL] in : {inp}")
        print(f"         out: {out}")
        print(f"         ожидалось '{must}', запрещено '{mustnot}'")
    return 0 if not failures else 1


if __name__ == "__main__":
    sys.exit(run())
